def привести_значение_к_списку(значение: Any) -> list[Any]:
    if значение is None:
        return []
    if isinstance(значение, list):
        return значение
    return [значение]


def проверить_и_очистить_разметку_канала(разметка: dict[str, Any]) -> tuple[dict[str, Any], list[str]]:
    """
    Мягкая проверка ответа ИИ.
    Возвращает очищенную разметку и список предупреждений.
    """
    очищенная = создать_пустую_разметку_канала()
    предупреждения: list[str] = []

    if not isinstance(разметка, dict):
        предупреждения.append("ИИ вернула не словарь JSON")
        return очищенная, предупреждения

    категория = разметка.get("категория", НЕЯСНО)
    if категория not in ГЛАВНЫЕ_КАТЕГОРИИ:
        предупреждения.append(f"Недопустимая категория: {категория}")
        категория = НЕЯСНО
    очищенная["категория"] = категория

                                       
    for поле in СВОБОДНЫЕ_ПОЛЯ:
        if поле in разметка:
            очищенная[поле] = разметка[поле]

                                                            
    поля_для_проверки = set(ОБЩИЕ_ПОЛЯ.keys())
    поля_для_проверки.update(получить_поля_категории(категория).keys())

    for поле in поля_для_проверки:
        if поле not in разметка:
            continue

        разрешённые = получить_разрешённые_значения(поле, категория)
        if not разрешённые:
            continue

        настройки = ОБЩИЕ_ПОЛЯ.get(поле) or получить_поля_категории(категория).get(поле, {})
        можно_несколько = bool(настройки.get("можно_несколько"))
        максимум = int(настройки.get("максимум", 3))

        if можно_несколько:
            значения = привести_значение_к_списку(разметка.get(поле))
            хорошие = [значение for значение in значения if значение in разрешённые]

            if len(хорошие) != len(значения):
                предупреждения.append(f"Поле '{поле}' содержало недопустимые значения")

            очищенная[поле] = хорошие[:максимум]
        else:
            значение = разметка.get(поле)
            if значение in разрешённые:
                очищенная[поле] = значение
            else:
                предупреждения.append(f"Поле '{поле}' содержало недопустимое значение: {значение}")
                очищенная[поле] = НЕЯСНО

                                                   
    try:
        уверенность = float(очищенная.get("уверенность", 0.0))
        очищенная["уверенность"] = max(0.0, min(1.0, уверенность))
    except (TypeError, ValueError):
        очищенная["уверенность"] = 0.0
        предупреждения.append("Уверенность была не числом")

    return очищенная, предупреждения
